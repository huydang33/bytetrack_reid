class BYTETracker(object):
    def __init__(self, args, frame_rate=30):
        self.tracked_stracks = []  # type: list[STrack]
        self.lost_stracks = []  # type: list[STrack]
        self.removed_stracks = []  # type: list[STrack]

        self.frame_id = 0
        self.args = args
        #self.det_thresh = args.track_thresh
        self.det_thresh = args.track_thresh + 0.1
        self.buffer_size = int(frame_rate / 30.0 * args.track_buffer)
        self.max_time_lost = self.buffer_size
        
        if self.args.matching == "reid":
            self.matching = matching.cosine_similarity
            self.tracklet = STrackFeature
            self.kalman_filter = KalmanFilterFeature()
        else:
            self.matching = matching.iou_distance
            self.tracklet = STrack
            self.kalman_filter = KalmanFilter()

    def get_feature_extracted(self, model, img, bounding_boxes):
        list_of_features = []
        for bounding_box in bounding_boxes:
            # Bounding box: [top-left x, top-left y, bottom-right x, bottom-right y]
            x, y, w, h = bounding_box
            _, _, H, W = img.size()

            # Calculate new bounding box coordinates to fit within image boundaries
            x1 = int(max(0, min(W, x)))
            y1 = int(max(0, min(H, y)))
            x2 = int(max(0, min(W, x + w)))
            y2 = int(max(0, min(H, y + h)))

            # Crop image
            cropped_image = img[:, :, y1:y2, x1:x2]
            # Nếu shape của bounding box có giá trị 0 ở chiều cao hoặc chiều ngang
            # thì sẽ tạo 1 tensor có giá trị bằng 0 để đại diện cho bounding box này
            if cropped_image.shape[2] == 0 or cropped_image.shape[3] == 0:
                list_of_features.append(torch.zeros(1, 2048, 14, 14))
                continue

            if self.args.feat_model_name == 'fastreid':
                # Chuyển đổi kích thước tensor
                resized_tensor = torch.nn.functional.interpolate(cropped_image, size=(224, 224), mode='bicubic', align_corners=False)
                with torch.no_grad():
                    features = model.backbone(resized_tensor.float())  # Trích xuất đặc trưng
            else:
                # Preprocess
                transform = transforms.Compose([
                    transforms.Resize((224, 224)),
                    transforms.ToTensor(),
                    transforms.Normalize(mean=[0.485, 0.456, 0.406], std=[0.229, 0.224, 0.225])
                ])
                pil_image = transforms.ToPILImage()(cropped_image.squeeze(0))
                # Forward pass through the model
                with torch.no_grad():
                    features = model(transform(pil_image).unsqueeze(0))

            list_of_features.append(features)
        return list_of_features

    def update(self, output_results, img_info, img_size, imgs, feature_extract_model):
        self.frame_id += 1
        activated_starcks = []
        refind_stracks = []
        lost_stracks = []
        removed_stracks = []

        if output_results.shape[1] == 5:
            scores = output_results[:, 4]
            bboxes = output_results[:, :4]
        else:
            output_results = output_results.cpu().numpy()
            scores = output_results[:, 4] * output_results[:, 5]
            bboxes = output_results[:, :4]  # x1y1x2y2
        img_h, img_w = img_info[0], img_info[1]
        scale = min(img_size[0] / float(img_h), img_size[1] / float(img_w))
        bboxes /= scale

        remain_inds = scores > self.args.track_thresh
        inds_low = scores > 0.1
        inds_high = scores < self.args.track_thresh

        inds_second = np.logical_and(inds_low, inds_high)
        dets_second = bboxes[inds_second]
        dets = bboxes[remain_inds]
        scores_keep = scores[remain_inds]
        scores_second = scores[inds_second]

        # from log import Logger, LOGGER
        # import log as logger
        # log = logger.LOGGER()
        # log.add("Bytetracker:update", {
        #     "bboxes": bboxes,
        #     "remain_inds": remain_inds,
        #     "inds_low": inds_low,
        #     "inds_high": inds_high,
        #     "inds_second": inds_second,
        #     "dets_second": dets_second,
        #     "dets": dets,
        #     "scores_keep": scores_keep,
        #     "scores_second": scores_second,
        # }).display()

        list_highscr_feat = self.get_feature_extracted(feature_extract_model, imgs, dets)

        if len(dets) > 0:
            '''Detections'''
            detections = [self.tracklet(self.tracklet.tlbr_to_tlwh(tlbr), s, feature) for
                          (tlbr, s, feature) in zip(dets, scores_keep, list_highscr_feat)]
        else:
            detections = []

        ''' Add newly detected tracklets to tracked_stracks'''
        unconfirmed = []
        tracked_stracks = []  # type: list[STrack]
        for track in self.tracked_stracks:
            if not track.is_activated:
                unconfirmed.append(track)
            else:
                tracked_stracks.append(track)

        ''' Step 2: First association, with high score detection boxes'''
        strack_pool = joint_stracks(tracked_stracks, self.lost_stracks)
        # Predict the current location with KF
        self.tracklet.multi_predict(strack_pool)

        dists = matching.iou_distance(strack_pool, detections)
        cosine_sim = matching.cosine_similarity(strack_pool, detections)
        if not self.args.mot20 and self.args.matching == 'iou':
            dists = matching.fuse_score(dists, detections)

        if self.args.matching == 'reid':
            cosine_sim = matching.fuse_motion(self.kalman_filter, cosine_sim, strack_pool, detections)
        
        # TBD: Kết hợp IoU và Cosine
        w_iou = 0.9
        w_cosine = 0.1
        if dists.size != 0 and cosine_sim.size != 0:
            combined_matrix = w_iou * dists + w_cosine * cosine_sim
        else:
            combined_matrix = dists 
        matches, u_track, u_detection = matching.linear_assignment(combined_matrix, thresh=self.args.match_thresh)

        for itracked, idet in matches:
            track = strack_pool[itracked]
            det = detections[idet]
            if track.state == TrackState.Tracked:
                track.update(detections[idet], self.frame_id)
                activated_starcks.append(track)
            else:
                track.re_activate(det, self.frame_id, new_id=False)
                refind_stracks.append(track)

        ''' Step 3: Second association, with low score detection boxes'''
        list_lowscr_feat = self.get_feature_extracted(feature_extract_model, imgs, dets_second)
        # association the untrack to the low score detections
        if len(dets_second) > 0:
            '''Detections'''
            detections_second = [self.tracklet(self.tracklet.tlbr_to_tlwh(tlbr), s, features) for
                          (tlbr, s, features) in zip(dets_second, scores_second, list_lowscr_feat)]
        else:
            detections_second = []
        r_tracked_stracks = [strack_pool[i] for i in u_track if strack_pool[i].state == TrackState.Tracked]
        dists = matching.iou_distance(r_tracked_stracks, detections_second)
        # dists = self.matching(r_tracked_stracks, detections_second)
        matches, u_track, u_detection_second = matching.linear_assignment(dists, thresh=0.5)
        # cosine_similarity = matching.cosine_similarity(r_tracked_stracks, detections_second)
        # matches, u_track, u_detection_second = matching.linear_assignment(cosine_similarity, thresh=0.5)
        for itracked, idet in matches:
            track = r_tracked_stracks[itracked]
            det = detections_second[idet]
            if track.state == TrackState.Tracked:
                track.update(det, self.frame_id)
                activated_starcks.append(track)
            else:
                track.re_activate(det, self.frame_id, new_id=False)
                refind_stracks.append(track)

        for it in u_track:
            track = r_tracked_stracks[it]
            if not track.state == TrackState.Lost:
                track.mark_lost()
                lost_stracks.append(track)

        '''Deal with unconfirmed tracks, usually tracks with only one beginning frame'''
        detections = [detections[i] for i in u_detection]
        dists = matching.iou_distance(unconfirmed, detections)
        if not self.args.mot20:
            dists = matching.fuse_score(dists, detections)
        matches, u_unconfirmed, u_detection = matching.linear_assignment(dists, thresh=0.7)
        # cosine_similarity = matching.cosine_similarity(unconfirmed, detections)
        # if not self.args.mot20:
            # cosine_similarity = matching.fuse_score(cosine_similarity, detections)
        # matches, u_unconfirmed, u_detection = matching.linear_assignment(cosine_similarity, thresh=0.7)
        for itracked, idet in matches:
            unconfirmed[itracked].update(detections[idet], self.frame_id)
            activated_starcks.append(unconfirmed[itracked])
        for it in u_unconfirmed:
            track = unconfirmed[it]
            track.mark_removed()
            removed_stracks.append(track)

        """ Step 4: Init new stracks"""
        for inew in u_detection:
            track = detections[inew]
            if track.score < self.det_thresh:
                continue
            track.activate(self.kalman_filter, self.frame_id)
            activated_starcks.append(track)
        """ Step 5: Update state"""
        for track in self.lost_stracks:
            if self.frame_id - track.end_frame > self.max_time_lost:
                track.mark_removed()
                removed_stracks.append(track)

        # print('Ramained match {} s'.format(t4-t3))

        self.tracked_stracks = [t for t in self.tracked_stracks if t.state == TrackState.Tracked]
        self.tracked_stracks = joint_stracks(self.tracked_stracks, activated_starcks)
        self.tracked_stracks = joint_stracks(self.tracked_stracks, refind_stracks)
        self.lost_stracks = sub_stracks(self.lost_stracks, self.tracked_stracks)
        self.lost_stracks.extend(lost_stracks)
        self.lost_stracks = sub_stracks(self.lost_stracks, self.removed_stracks)
        self.removed_stracks.extend(removed_stracks)
        self.tracked_stracks, self.lost_stracks = remove_duplicate_stracks(self.tracked_stracks, self.lost_stracks)
        # get scores of lost tracks
        output_stracks = [track for track in self.tracked_stracks if track.is_activated]

        return output_stracks


def joint_stracks(tlista, tlistb):
    exists = {}
    res = []
    for t in tlista:
        exists[t.track_id] = 1
        res.append(t)
    for t in tlistb:
        tid = t.track_id
        if not exists.get(tid, 0):
            exists[tid] = 1
            res.append(t)
    return res


def sub_stracks(tlista, tlistb):
    stracks = {}
    for t in tlista:
        stracks[t.track_id] = t
    for t in tlistb:
        tid = t.track_id
        if stracks.get(tid, 0):
            del stracks[tid]
    return list(stracks.values())


def remove_duplicate_stracks(stracksa, stracksb):
    pdist = matching.iou_distance(stracksa, stracksb)
    pairs = np.where(pdist < 0.15)
    dupa, dupb = list(), list()
    for p, q in zip(*pairs):
        timep = stracksa[p].frame_id - stracksa[p].start_frame
        timeq = stracksb[q].frame_id - stracksb[q].start_frame
        if timep > timeq:
            dupb.append(q)
        else:
            dupa.append(p)
    resa = [t for i, t in enumerate(stracksa) if not i in dupa]
    resb = [t for i, t in enumerate(stracksb) if not i in dupb]
    return resa, resb
